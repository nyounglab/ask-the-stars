"""
Google Gemini API를 사용한 점성술 차트 해석 모듈
"""

import logging
import os

import google.generativeai as genai

logger = logging.getLogger(__name__)


class AstrologyInterpreter:
    """Google Gemini API를 사용하여 점성술 차트를 해석하는 클래스"""

    def __init__(self, api_key: str | None = None):
        """
        Args:
            api_key: Google API 키 (None일 경우 환경 변수에서 로드)
        """
        self.api_key = api_key or os.getenv("GOOGLE_API_KEY")
        if not self.api_key:
            raise ValueError("GOOGLE_API_KEY가 설정되지 않았습니다.")

        # Gemini API 설정
        genai.configure(api_key=self.api_key)

        # 모델 설정 (gemini-2.5-flash: 최신 모델, 빠르고 무료)
        self.model = genai.GenerativeModel('gemini-2.5-flash')
        self.request_timeout = 120  # 초

    def interpret_chart(self, chart_data: str, user_name: str = "사용자") -> dict[str, str]:
        """
        점성술 차트 데이터를 Gemini API로 해석합니다.

        Args:
            chart_data: 계산된 차트 데이터
            user_name: 사용자 이름 (선택)

        Returns:
            해석 결과를 담은 딕셔너리
            {
                "interpretation": "해석 텍스트",
                "error": "에러 메시지 (있을 경우)"
            }
        """
        try:
            # 전체 프롬프트 구성
            full_prompt = self._build_prompt(chart_data, user_name)

            # Gemini API 호출 (타임아웃 설정)
            response = self.model.generate_content(
                full_prompt,
                request_options={"timeout": self.request_timeout},
            )

            # Safety filter 차단 확인
            if not response.candidates:
                return {
                    "interpretation": None,
                    "error": "AI가 응답을 생성하지 못했습니다. 다시 시도해주세요."
                }

            candidate = response.candidates[0]
            if candidate.finish_reason and candidate.finish_reason.name == "SAFETY":
                return {
                    "interpretation": None,
                    "error": "AI 안전 필터에 의해 응답이 차단되었습니다. 다시 시도해주세요."
                }

            # 응답 텍스트 추출
            interpretation = response.text

            return {
                "interpretation": interpretation,
                "error": None
            }

        except Exception as e:
            error_msg = str(e)
            if "timeout" in error_msg.lower() or "timed out" in error_msg.lower() or "deadline" in error_msg.lower() or "504" in error_msg:
                error_msg = "AI 응답 시간이 초과되었습니다. 잠시 후 다시 시도해주세요."
            else:
                logger.error("AI 해석 에러: %s", error_msg)
                error_msg = "AI 해석 중 오류가 발생했습니다. 잠시 후 다시 시도해주세요."
            return {
                "interpretation": None,
                "error": error_msg
            }

    def _build_prompt(self, chart_data: str, user_name: str) -> str:
        """전체 프롬프트 구성 (8개 카테고리, ===구분자=== 형식)"""
        system_instructions = """당신은 프리미엄 점성술 브랜드의 전속 해설가입니다.
        출생 차트를 읽고, 읽는 이가 조용히 빠져들게 만드는 격조 있고 입체적인 해석을 제공합니다.

        해석 스타일:
        - 고급스럽고 신비로운 분위기를 유지하되, 과장하지 않을 것
        - 단정하고 여백이 느껴지는 문장. 짧고 밀도 있는 문장 위주로 서술
        - 운명을 확정짓지 말 것. 별은 경향성을 보일 뿐, 삶의 주도권은 개인에게 있음을 은연중에 드러낼 것 ("~일 수 있습니다", "~의 가능성을 품고 있습니다")
        - 독자가 스스로를 투영할 수 있도록, 2인칭 서술로 조용히 말을 건네는 톤. 다만 "당신"을 매 문장 반복하지 말 것. 주어를 생략하거나 변주하여 리듬감 있게
        - 위로와 통찰, 약간의 신비감을 담을 것
        - 광고나 기계적인 텍스트처럼 보이지 않게. 은근히 설득력 있는 문장을 지향
        - **굵은 표현**으로 핵심 키워드를 절제 있게 강조. 한 단락에 최대 2-3개
        - **모순의 통합**: 차트 내에 상반된 에너지(예: 불의 태양과 물의 달)가 있다면, 이를 단순한 갈등이 아닌 '그 사람만의 입체적인 매력'이자 '성장의 동력'으로 우아하게 해석할 것.
        - 시적이거나 은유적인 표현을 섞되, 난해하지 않게 직관적으로 와닿도록 작성
        - 카테고리 간 톤의 온도를 미세하게 달리할 것: 종합/인생흐름은 관조적이고 깊게, 연애/관계는 따뜻하게, 커리어/재물은 차분하고 명료하게

        전문성 및 정확도 규칙:
        - 해석의 근거가 되는 **점성술적 지표(행성, 별자리, 하우스, 어스펙트 등)**를 문장 속에 자연스럽게 녹여낼 것. 독자가 '이래서 이런 해석이 나오는구나'하고 납득할 수 있어야 함.
        - 차트 내에 상반되는 에너지(예: 불의 성향과 물의 성향이 공존)가 있다면, 이를 단순한 모순으로 취급하지 말고 "내면의 고요함과 외면의 열정이 교차하는 입체적인 결"처럼 매력적인 복합성으로 풀어낼 것.

        좋은 문장 예시:
        - "태양은 전갈자리에 머물고 있습니다. 깊이를 모르는 물처럼, 표면에 보이는 것 이상의 세계를 품고 있을지도 모릅니다."
        - "금성이 7하우스에 자리한 배치입니다. 관계 속에서 아름다움을 발견하는 감각이 자연스러운 것일 수 있습니다."
        - "불(Aries)의 에너지로 시작된 열정을, 흙(Taurus)의 달이 차분히 현실로 빚어내는 고유한 리듬을 가지고 있습니다."

        피해야 할 문장 예시:
        - "전갈자리 태양이시네요! 미스터리한 매력 뿜뿜" (가볍고 과장된 톤)
        - "연애 고수의 기질이 넘치시네요" (단정적이고 캐주얼한 표현)
        - "당신의 태양은... 당신의 달은... 당신의 금성은..." (당신 반복으로 단조로움)

        ---

        """

        user_instructions = f"""다음은 {user_name}님의 출생 차트 데이터입니다:

        {chart_data}

        위 차트 데이터를 분석하여 아래 8개 카테고리로 나누어 깊이 있게 해석해주세요.

        **필수 출력 형식:**
        1. 각 카테고리는 반드시 ===카테고리명=== 구분자로 시작 (앞뒤 공백 없이 정확히)
        2. 구분자 바로 다음 줄에 ### 소제목 작성
        3. 소제목은 해당 분석 결과를 꿰뚫는, 여운이 남는 은유적이고 시적인 한 줄 (예: "깊은 물 위에 뜬 고요한 달", "씨앗이 어둠 속에서 방향을 찾는 법")
        4. 내용은 각 300-500자 내외로, **굵은 표현**과 - 불릿을 적절히 활용하여 가독성을 높일 것
        5. 아래 8개 카테고리명을 정확히 사용할 것

        ===종합===
        ### [이 사람의 본질을 한 줄로 표현한 시적인 소제목]
        개인의 성격과 본질(Who you are)을 다루는 총론.
        - **나의 엔진(태양)**: 세상을 바라보는 방식과 삶에 대한 기본적인 태도, 핵심 동력
        - **나의 내면(달)**: 무엇이 이 사람을 움직이게 하는지, 내면적 욕구와 정서적 안정감을 찾는 방법
        - **나의 세상(상승점, AC)**: 세상과 만나는 첫 번째 창문, 타인이 처음 만났을 때 느끼는 에너지의 결
        - 주요 어스펙트나 두드러지는 원소(불/물/흙/공기)의 비율이 있다면 그 고유한 색채를 짚어줄 것

        ===성격===
        ### [내면의 결을 은유적으로 표현한 소제목]
        잠재력과 재능, 심리적 동기를 깊이 있게 다룸.
        - 태양/달/수성의 조화를 기반으로, 이 사람이 세상을 이해하고 정보를 처리하는 고유한 방식
        - 내면과 외면 사이의 온도차: 세상에 보여지는 모습과 그 안쪽에 흐르는 실제 감정의 대비가 있다면 이를 매력으로 서술
        - 숨겨진 재능: 미처 스스로 인지하지 못했을, 차트가 조용히 가리키고 있는 강점과 약점

        ===커리어===
        ### [사회적 역할과 천직을 시적으로 표현한 소제목]
        사회적 지향점과 천직(Career & Vocation)을 다룸.
        - **사회적 페르소나(MC, 10하우스)**: 남들에게 보여지는 직업적 아우라와 사회에서 인정받기 쉬운 포지션
        - **영혼의 만족감을 주는 일**: 6하우스(일상적 노동)와 10하우스, 토성/목성을 기반으로 단순히 돈을 버는 일을 넘어 성취감을 느낄 수 있는 활동 영역 제안
        - **일하는 방식과 소통**: 수성과 화성 기반. 목표를 향해 나아가는 추진력과 사람들과 협업(또는 독립)하는 스타일

        ===연애===
        ### [사랑의 패턴을 은유적으로 표현한 소제목]
        연애운과 사랑의 방식을 다룸.
        - 금성과 화성, 5하우스 기반의 사랑의 언어: 무엇에 매력을 느끼고, 자신의 마음을 어떻게 표현하는지
        - 사랑 안에서 이 사람이 보여주는 고유한 템포 (천천히 스며드는지, 불꽃처럼 타오르는지)
        - 연애 관계에서 특히 주의해야 할, 혹은 치유받을 수 있는 무의식적 패턴
        - 현재 중요한 행성 트랜짓이 있다면 연애운의 새로운 흐름이나 변화의 단서를 조심스럽게 언급

        ===건강===
        ### [몸과 마음의 균형을 시적으로 표현한 소제목]
        신체적 활력과 정서적 웰빙을 다룸.
        - 6하우스(건강과 루틴), 태양(생명력), 달(정서) 기반의 체질적 특성
        - **에너지 충전의 방식**: 스트레스를 받을 때 어디서 에너지가 새어나가며, 몸과 마음의 균형을 되찾기 위해 필요한 환경이나 휴식법
        - 화성의 위치를 통해 보는 일상 속 활력의 기복과 이를 건강하게 해소하는 방법

        ===재물===
        ### [풍요와 흐름을 은유적으로 표현한 소제목]
        재물운과 물질적 가치관을 다룸.
        - 2하우스(나의 자산), 8하우스(타인/공유 자산), 목성 기반의 금전 감각
        - **풍요가 머무는 자리**: 이 사람에게 가장 자연스럽게 수입이 창출되는 패턴과 돈을 대하는 태도
        - 물질을 단순히 소유하는 것을 넘어, 삶을 어떻게 더 풍요롭게 만드는 데 사용할 수 있는지에 대한 통찰

        ===관계===
        ### [사람과 사람 사이의 결을 시적으로 표현한 소제목]
        타인과 맺는 사회적 관계와 파트너십을 다룸.
        - 7하우스(1:1 파트너십), 11하우스(커뮤니티/친구), 달을 기반으로 한 타인과의 소통 방식
        - **관계의 역학**: 어떤 부류의 사람들과 있을 때 시너지가 나고, 어떤 상황에서 경계선이 필요한지
        - 인간관계에서 고립을 느끼기 쉬운 지점이 있다면, 이를 극복했을 때 얻어지는 내면의 성장

        ===인생흐름===
        ### [영혼의 여정을 은유적으로 표현한 소제목]
        카르마와 영혼의 목적(Nodes), 그리고 인생의 굵직한 테마를 다룸.
        - **익숙한 쉼터(사우스 노드)**: 과거로부터 가져와 이미 너무나 편안하고 익숙해진 패턴. 가끔은 성장을 막는 안주처
        - **나아갈 방향(노스 노드)**: 낯설고 두렵지만, 이번 생의 영혼이 진정으로 도달하고자 하는 성장의 종착지
        - 차트 내에 키론(Chiron)이나 강한 토성의 영향력이 있다면, 상처와 시련이 어떻게 인생의 단단한 지혜로 승화될 수 있는지
        - 현재 삶의 어느 계절을 지나고 있는지, 관조적이고 깊이 있는 마무리 인사
        """
        return system_instructions + user_instructions
